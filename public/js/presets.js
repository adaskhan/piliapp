// Get list of all presets for footer navigation
function getAllPresets() {
  return Object.entries(PRESETS).map(([key, value]) => ({
    key,
    ...value
  }));
}

// Get preset by key
function getPreset(key) {
  return PRESETS[key] || PRESETS.wheel;
}

// Get current page preset key from URL
function getCurrentPresetKey() {
  const path = window.location.pathname.split('/').pop() || 'index.html';
  const preset = Object.entries(PRESETS).find(([_, p]) => p.url === path);
  return preset ? preset[0] : 'wheel';
}
